<p align="center"><b><font face="Arial" color="#FF0000">HELP SCREEN (Line 
Inventory Manager)</font></b><br>
<img border="0" src="images/hlp_inv_mgr2.jpg" width="790" height="278"></p>
<p align="left"><font face="Arial"><b><font color="#FF0000">1) Filter</font></b> 
The filter will allow you to select only the records you want to see locations, 
divisions, departments, BTN's that need review or adds moves and changes which 
are not complete. Extremely useful if you have a large number of records.<br>
<font color="#FF0000"><b>2) Adds, Moves &amp; Changes</b></font> (AMC) This button 
takes you to another screen where you can see a list of any orders you have 
placed.&nbsp; You can order new service from this screen or ask a question about 
your account.<br>
<b><font color="#FF0000">3) Admin</font></b> This page is where your 
administrator chooses who will have access to your records and who will be 
notified of new orders.<br>
<b><font color="#FF0000">4) Archive</font></b> This database keeps a record of 
all your previous billing telephone numbers (BTN's).&nbsp; You can see how many 
lines you had at a certain location last year.<br>
<b><font color="#FF0000">5) Results per page</font></b> If you have hundreds or 
thousands of BTN's it will take more time to view (download) the records.&nbsp; 
You can select to look at just 50 records per page.<br>
<b><font color="#FF0000">6) Edit</font></b> This is where you go to edit the 
information on each BTN.<br>
<b><font color="#FF0000">7) BTN</font></b> This is the Billing Telephone Number 
on you account. You can sort by BTN but you can not modify this field.<br>
<b><font color="#FF0000">8) Last Updated</font></b> This field shows when your 
records were retrieved from the telephone company<br>
<b><font color="#FF0000">9) Location Name</font></b> This field defaults to the 
address on the CSR.&nbsp; Each customer should modify the location name so that 
it has significance to them.&nbsp; By clicking on Location Name your records 
will be sorted in ascending order, if you click twice your records will be 
sorted in descending order.&nbsp; You can also use the filter to select only one 
location.<br>
<b><font color="#FF0000">10) Division</font></b> This field is optional.&nbsp; 
It will be empty when you first review your records.&nbsp; You can select and 
sort by division if needed.<br>
<font color="#FF0000"><b>11) Department</b></font> Similar to division this 
field is empty to start with but is critical when keeping profit centers apart. 
You may filter and sort by department.<br>
<b><font color="#FF0000">12) Adds Moves &amp; Changes</font></b> This field will 
show you the status of any adds moves or changes.&nbsp; Order has not been 
started yet<br>
(red dot), order is in Process (yellow dot), order complete (green dot)<br>
<b><font color="#FF0000">13) Description of Service</font></b> The contents of 
this field are entered by the person who reviews you records. You may change or 
modify this description.<br>
<b><font color="#FF0000">14) Contract Expiration Date</font></b> To better 
manage your records the date is pulled from your CSR and entered here.&nbsp; You 
may sort or modify this field.<br>
<font color="#FF0000"><b>15) Cost</b></font> The cost is taken from your CSR. 
You may sort by this field.<br>
<b><font color="#FF0000">16) Review</font></b> When your records are pulled from 
the telephone company an Account Consultant reviews each records for any errors 
or problems.&nbsp; If your records are fine then a green check is placed in this 
field. If there is a problem then a red check would appear.&nbsp; It will be the 
customers responsibility to assign someone to fix these problems, an account 
manager, an agent or a consultant.<br>
<b><font color="#FF0000">17) CSR Summary</font></b> This is a critical page in 
reviewing your records.&nbsp; This page totals all the different USOC codes and 
pricing,&nbsp; then looks up the description of the codes and displays this 
information in an easy( easier) to understand format.<br>
<font color="#FF0000"><b>18) CSR Detail</b></font> This is the actual Customer 
Service Record (CSR) that the telephone company provides.&nbsp; Many times an 
Account Consultant will need to review CSR in detail (try to read this 
yourself).</font></p>
